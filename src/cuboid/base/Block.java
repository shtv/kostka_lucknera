package cuboid.base;

public class Block {
		private int maxX;
		private int maxY;
		private int maxZ;
		private int cuboidsCount;
		
		public int getMaxX()
		{
			return maxX;
		}

		public int getMaxY() {
			return maxY;
		}

		public int getMaxZ() {
			return maxZ;
		}
		
		public int getCuboidsCount()
		{
			return cuboidsCount;
		}
}
